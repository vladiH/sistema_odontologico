    private void btnCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearMouseClicked
        if (txtRepitaClave.isEnabled()) {
            if (txtClave.getText().equals(txtRepitaClave.getText())) {
                if (txtCOP.getText().length() == 5) {
                    //if(!txtAP.equals("") && !txtAM.equals("")&& !txtNombre.equals("")&&!txtCOP.equals("")){}
                    EDoctor.setApellidoPaterno(txtAP.getText());
                    EDoctor.setApellidoMaterno(txtAM.getText());
                    EDoctor.setNombre(txtNombre.getText());
                    EDoctor.setCOP(txtCOP.getText());
                    EDoctor.setCelularClaro(txtCelularClaro.getText());
                    EDoctor.setCelularMovistar(txtCelularMovistar.getText());
                    EDoctor.setDireccion(txtDireccion.getText());
                    EDoctor.setDepartamento(txtDepartamento.getText());
                    EDoctor.setCorreo(txtCorreo.getText());
                    EDoctor.setUsuario(txtUsuario.getText());
                    EDoctor.setContrasena(txtClave.getText());
                    if (CDoctor.Guardar()) {
                        Errores.Exito("Datos correctamente ingresados");
                        dispose();
                    } else {
                        Errores.Error("Datos no ingresados al sistema");

                    }
                } else {
                    Errores.Error("COP incorrecto");
                }
            } else {
                Errores.Error("Las claves no contrastan");
            }
        } else {
            if (txtNuevaClave.getText().equals(txtRepitaNuevaClave.getText())) {
                EDoctor.setUsuario(txtUsuario.getText());
                EDoctor.setContrasena(txtNuevaClave.getText());
                EDoctor.setNombre(txtClave.getText());
                if (CDoctor.Actualizar()) {
                    Errores.Exito("Datos actualizados correctamente");
                } else {
                    Errores.Error("Datos no actualizados\n Ocurrio un error");
                }
                dispose();
            } else {
                Errores.Error("No contrastan las claves nuevas");
            }

        }
    }//GEN-LAST:event_btnCrearMouseClicked

