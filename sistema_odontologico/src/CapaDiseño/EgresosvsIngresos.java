    private void btnConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConsultarMouseClicked
        String M1 = String.valueOf(FechaMes.getMonth() + 1);
        String M2 = String.valueOf(FechaMes2.getMonth() + 1);
        String A1 = String.valueOf(FechaAnno.getYear());
        String A2 = String.valueOf(FechaAnno2.getYear());

        if (Integer.parseInt(M1) < 10) {
            M1 = "0" + M1;
        }
        if (Integer.parseInt(M2) < 10) {
            M2 = "0" + M2;
        }
        EEgresosVsIngresos.setFechaIngresos(A1 + "-" + M1);
        EEgresosVsIngresos.setFechaEgresos(A2 + "-" + M2);
        CGraficarIngresosVsEgresos g = new CGraficarIngresosVsEgresos();
        panelGrafico.removeAll();
        panelGrafico.add(g.graficar());
        panelGrafico.repaint();
    }//GEN-LAST:event_btnConsultarMouseClicked


