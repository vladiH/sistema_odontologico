    public void NuevaCitaMostrar() {
        try {
            String Fecha = "";
            String[] Hora;
            SimpleDateFormat date = new SimpleDateFormat("dd-MM-yyyy");
            NuevaCita nc = new NuevaCita();
            nc.btnBuscar.setVisible(noVisible);
            nc.btnGuardar.setVisible(noVisible);
            nc.rbCR.setEnabled(noVisible);
            nc.rbNC.setEnabled(noVisible);
            nc.txtAP.setEnabled(noVisible);
            nc.txtAM.setEnabled(noVisible);
            nc.txtN.setEnabled(noVisible);
            nc.txtDNI.setEnabled(noVisible);
            nc.txtT.setEnabled(noVisible);
            ResultSet rs = CCita.BuscarId();
            while (rs.next()) {
                nc.txtAP.setText(rs.getString(1));
                nc.txtAM.setText(rs.getString(2));
                nc.txtN.setText(rs.getString(3));
                nc.txtDNI.setText(rs.getString(4));
                nc.txtT.setText(rs.getString(5));
                Hora = rs.getString(6).split(" ");
                nc.txtHoraReserva.setText(Hora[0]);
                nc.cbHorario.setSelectedItem(Hora[1]);
                Date dateValue;
                try {
                    Fecha = rs.getDate(7).toString();
                    if (Fecha.equals("")) {
                        dateValue = date.parse(Errores.FormatoFecha("0001-01-01"));
                    } else {
                        dateValue = date.parse(Errores.FormatoFecha(Fecha));
                    }
                    nc.txtFecha.setDate(dateValue);
                } catch (Exception ex) {
                    Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
                }
                nc.txtMotivoConsulta.setText(rs.getString(8));
            }
            nc.setLocationRelativeTo(null);
            nc.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(Cita.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

