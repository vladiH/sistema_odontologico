    @Override
    public int print(Graphics graf, PageFormat pagfor, int page) throws PrinterException {
        if (page > 0) {
            return NO_SUCH_PAGE;
        }
        Graphics2D bot = (Graphics2D) graf;
        bot.translate(pagfor.getImageableX() + 50, pagfor.getImageableY() + 50);
        bot.scale(0.5, 0.5);
        paBoleta.printAll(graf);
        return PAGE_EXISTS;
    }
