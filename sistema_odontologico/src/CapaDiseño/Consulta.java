    public void Inicio() {
        ResultSet rs = CConsulta.RecuperarInformacion();
        if (rs == null) {
            Errores.Error("No se puede recuperar informacion personal en consulta");
        } else {
            try {
                while (rs.next()) {
                    txtNombreCompleto.setText(rs.getString(1));
                    txtCop.setText(rs.getString(2));
                    txtCelularClaro.setText(rs.getString(3));
                    txtCelularMovistar.setText(rs.getString(4));
                    txtDireccion.setText(rs.getString(5));
                    txtRegion.setText(rs.getString(6));
                    txtCorreo.setText(rs.getString(7));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Consulta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
