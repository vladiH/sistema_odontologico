    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        try {
            PrinterJob trab = PrinterJob.getPrinterJob();
            trab.setPrintable(this);
            boolean impr = trab.printDialog();

            if (impr) {
                trab.print();
            }
        } catch (PrinterException pex) {
            Errores.Error("Error\n" + pex);
        }
        Limpiar();
    }//GEN-LAST:event_btnImprimirActionPerformed
    public void Limpiar() {
        txtApellidoMaterno.setText("");
        txtApellidoPaterno.setText("");
        txtDNI.setText("");
        txtFecha.setCalendar(null);
        txtNombre.setText("");
        txtTotal.setText("0");
        tbConsulta.removeAll();
        tbConsulta.repaint();
    }
    private void tbConsultaFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tbConsultaFocusGained
        double Total = 0;
        String pieza = "";
        String costo = "";
        int Columnas = tbConsulta.getRowCount();
        for (int i = 0; i < Columnas; i++) {
            if (tbConsulta.getValueAt(i, 2) == null) {
                break;
            } else {
                if (tbConsulta.getValueAt(i, 0) == null) {
                    pieza = "0";
                } else {
                    pieza = tbConsulta.getValueAt(i, 0).toString();
                }
                if (tbConsulta.getValueAt(i, 2) == null) {
                    costo = "0";
                } else {
                    costo = tbConsulta.getValueAt(i, 2).toString();
                }
                Total = Total + Double.parseDouble(pieza) * Double.parseDouble(costo);

                txtTotal.setText(String.valueOf(Total));
            }
        }
    }//GEN-LAST:event_tbConsultaFocusGained

