    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        int seleccion1 = JOptionPane.showOptionDialog(null, "¬øseguro decea Guardar?"/*componente padre*/, //Mensaje
                "Seleccione una opci√≥n", // T√≠tulo
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null, // null para icono por defecto.
                new Object[]{"Si", "No"}, // null para YES, NO y CANCEL
                "Si");
        if (seleccion1 != -1) {
            if ((seleccion1 + 1) == 1) {
                if (rbNC.isSelected()) {
                    if (txtAP.getText().equals("") || txtAM.getText().equals("") || txtN.getText().equals("") || txtDNI.getText().equals("") || txtHoraReserva.getText().equals("") || txtFecha.getCalendar() == null || txtMotivoConsulta.getText().equals("")) {
                        Errores.Error("Los campos en rojo son obligatorios");
                    } else {
                        ECliente.setApellidoPaterno(txtAP.getText());
                        ECliente.setApellidoMaterno(txtAM.getText());
                        ECliente.setNombre(txtN.getText());
                        ECliente.setDNI(txtDNI.getText());
                        if (txtT.getText().equals("")) {
                            ECliente.setTelefono("0");
                        } else {
                            ECliente.setTelefono(txtT.getText());
                        }
                        ECita.setHora(txtHoraReserva.getText() + " " + cbHorario.getSelectedItem().toString());
                        ECita.setFecha(Errores.FormatoFecha(String.valueOf(txtFecha.getCalendar().get(Calendar.DAY_OF_MONTH) + "-"
                                + (txtFecha.getCalendar().get(Calendar.MONTH) + 1) + "-"
                                + txtFecha.getCalendar().get(Calendar.YEAR))));
                        ECita.setMotivo(txtMotivoConsulta.getText());
                        ECliente.setEdad("0");
                        ECliente.setFechaNacimiento("0001-01-01");
                        ECliente.setDireccion("");
                        ECliente.setParentesco("");
                        if (CCita.Guardar1()) {

                            String[] Fecha = Cliente.lbFecha.getText().split("-");
                            ECita.setFecha(Fecha[2] + "-" + Fecha[1]);
                            CCita.Listar_Por_Fecha();
                            CCliente.Listar();
                            Errores.Exito("Datos guardado correctamente");
                            dispose();
                        } else {
                            Errores.Error("Ocurrio un error al intentar guardar ");
                        }
                    }
                } else {
                    if (txtHoraReserva.getText().equals("") || txtFecha.getCalendar() == null || txtMotivoConsulta.getText().equals("")) {
                        Errores.Error("Los campos de hora, fecha, motivo son obligatorios");
                    } else {
                        ECita.setHora(txtHoraReserva.getText() + " " + cbHorario.getSelectedItem().toString());
                        ECita.setFecha(Errores.FormatoFecha(String.valueOf(txtFecha.getCalendar().get(Calendar.DAY_OF_MONTH) + "-"
                                + (txtFecha.getCalendar().get(Calendar.MONTH) + 1) + "-"
                                + txtFecha.getCalendar().get(Calendar.YEAR))));
                        ECita.setMotivo(txtMotivoConsulta.getText());
                        if (CCita.Guardar()) {
                            String[] Fecha = Cliente.lbFecha.getText().split("-");
                            ECita.setFecha(Fecha[2] + "-" + Fecha[1]);
                            CCita.Listar_Por_Fecha();
                            CCliente.Listar();
                            Errores.Exito("Cita guardada correctamente");
                            dispose();
                        } else {
                            Errores.Error("Ocurrio un error al intentar guardar ");
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnGuardarMouseClicked

